package practice.programs.day1;

public class DataTypes {

	public static void main(String[] args) {
		// Write a Java program to print an int, a double and a char on screen.
		
		int i = 10;
		double d = 10.33;
		char c = 'a';
		
		System.out.println(i);
		System.out.println(d);
		System.out.println(c);
		// we can print in one single line using string concatenation
		System.out.println("Integer i:"+ i + "\ndouble d:" + d + "\nchar c:"+ c);
		

	}

}
